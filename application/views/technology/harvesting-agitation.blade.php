		<h3 class="page-header">3R's continual Floc harvester</h3>

		<p>Reduce, Reuse, Recycle has been deeply etched into our engineering that even our harvesting adopts them. Using an ingenious proprietary process we are able to reuse a flocculant more than once, drastically reducing the amount of flocculant needed. The same ingenious process auto corrects the medium, so that the water can be recycled into growing algae.</p>
		<p>These 3 R's reduce, reuse, recycle have been the foundation for creating sustainable and cost effective systems, we believe it is the approach for the future to come.</p>

		<h3 class="page-header">Macro Biology assisted Agitation systems</h3>

		<p>One of the major costs of a modern algae production system is the agitation, we knew it that without a cost effective agitation system it would not be possible to scale. We looked at various traditions in China and India then compared them to how nature was doing it and we had our answer. We selected a few organisms which would grow along with the algae to keep it floating and well agitated. With our Macro Biology assisted Agitation systems we have entered a new era of biotechnology based systems.</p>

		<p>Due to the combination of our HAASE system and the biological assisted system GreenBubble has a winning formula of cost effective production systems. The Macro Biology assisted Agitation systems has the following efficiency's that other agitations do not match:</p>
		
