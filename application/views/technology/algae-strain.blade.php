		<h3 class="page-header">Natural Selection and Evolution (NSE)</h3>

		<p>THEORY OF EVOLUTION forms the basis for the selection of our algae strains. We make wild strains compete against each other and choose the ones which are most suitable for the conditions present in our system. The algae selected are then evolved to adapt and fit into our system. The end product of our NSE process is an algae strain which grows very fast(doubles every 4 hours) and is extremely robust. An effective algae selection process forms a very strong foundation for our algae cultivation system.</p>

		<h3 class="page-header">Heterotrophic and Autotrophic Algae Polyculture</h3>

		<p>Unity is strength and division is weakness. It is better to have multiple algae species working together to defend against a multitude of enemies rather than a single specie. We grow different species of algae which help each other and keep unwanted algae out of our system.</p>
		<p>In a typical Green Bubble system a culture medium consists of algae which grow both in the morning and in the night, among which few grow on sunlight and others on waste sugars. So in 24 hours we get the maximum biomass.</p>
