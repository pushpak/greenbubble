		<h3 class="page-header">Bio Based Control Systems (BBCS)</h3>

		<p>The realization that microbial algae can be controlled most effectively using microbes has helped in devising the BBCS mechanism The BBCS mechanism is used by us GreenBubble to guard and assist our algae culture. In BBCS mechanism we select microbes that are predators of wild algae, these microbes weed out wild algae acting as guardians to our algae cultures. In addition to this we have discovered various microbe inducers and aquatic creature which help ward of pests and assist algae growth.</p>

