		<h3 class="page-header">Hybrid ad-hoc algae scale easy systems (HAASE)</h3>

		<p>With experience in cultivating in open ponds, Race Ways and Photo Bio-reactors, we have come to realize that we can combine the advantages of these into a single system. This realization has led us to create HAASE. Our HAASE system has individual pods which are grouped into various ad-hoc groups. Each Ad-Hoc group has individual pods which interact within the group for sharing nutrients, harvesting and diagnosis.</p>

		<p>HAASE is a designed to provide:</p>

		<ul>
			<li>A layer of security against contamination.</li>
			<li>Preventing culture crash propagation.</li>
			<li>Efficient nutrient distribution.</li>
			<li>Efficient nutrient utilization.</li>
			<li>Highly effective agitation.</li>
		</ul>

		<h3 class="page-header">Low cost and reliable design</h3>

		<p>To create a positive and lasting impact in sustainability and environment, we had to include the consumables market  among our target markets. For any industry to enter the consumable market the costs have to be reduced to a bare minimum. Among the major barriers in algae for making it low cost was the capital cost, our goal was to reduce it to a low, that was unheard of in the algae industry. With huge experience in structural engineering and low cost buildings, we were able to reduce the cost to a small factor to that of a open pond, which is the current lowest cost for set-up.</p>

		<p>Expertise in the domains of low cost housing, structural engineering of high end machines  and material science, has helped us design a system that is not only low cost but also very Robust. Using Ingenious methodologies we have been able to enhance the life term of many commonly use materials, letting us use it for a much longer time than they were designed for. The extension of the life has let us provide long lasting, robust and cost effective structures to cultivate the algae.</p>


